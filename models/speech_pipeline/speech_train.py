class HuBERTBiLSTM(nn.Module):

    def __init__(
        self,
        hidden_size=128,
        dropout=0.3,
        num_classes=7
    ):

        super().__init__()

        self.hubert = HubertModel.from_pretrained(
            "facebook/hubert-base-ls960"
        )

        for param in self.hubert.parameters():
            param.requires_grad = False

        self.bilstm = nn.LSTM(
            input_size=768,
            hidden_size=hidden_size,
            num_layers=2,
            batch_first=True,
            bidirectional=True,
            dropout=dropout
        )

        self.fc1 = nn.Linear(
            hidden_size * 2,
            256
        )

        self.relu = nn.ReLU()

        self.dropout = nn.Dropout(
            dropout
        )

        self.fc2 = nn.Linear(
            256,
            num_classes
        )

    def forward(
        self,
        input_values
    ):

        with torch.no_grad():

            hubert_output = self.hubert(
                input_values=input_values
            )

            features = (
                hubert_output
                .last_hidden_state
            )

        lstm_out, _ = self.bilstm(
            features
        )

        embedding = lstm_out[:, -1, :]

        x = self.fc1(
            embedding
        )

        x = self.relu(
            x
        )

        x = self.dropout(
            x
        )

        logits = self.fc2(
            x
        )

        return (
            logits,
            embedding
        )


