class BERTEmotionClassifier(nn.Module):

    def __init__(
        self,
        dropout=0.3,
        num_classes=7
    ):

        super().__init__()

        self.bert = BertModel.from_pretrained(
            "bert-base-uncased"
        )

        self.fc1 = nn.Linear(
            768,
            256
        )

        self.relu = nn.ReLU()

        self.dropout = nn.Dropout(
            dropout
        )

        self.fc2 = nn.Linear(
            256,
            num_classes
        )

    def forward(
        self,
        input_ids,
        attention_mask
    ):

        outputs = self.bert(
            input_ids=input_ids,
            attention_mask=attention_mask
        )

        cls_embedding = (
            outputs.last_hidden_state[:,0,:]
        )

        x = self.fc1(
            cls_embedding
        )

        x = self.relu(x)

        x = self.dropout(x)

        logits = self.fc2(x)

        return logits, cls_embedding

