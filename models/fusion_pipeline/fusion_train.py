class FusionEmotionModel(nn.Module):

    def __init__(
        self,
        dropout=0.3,
        num_classes=7
    ):

        super().__init__()

        # Speech embedding = 256
        # Text embedding = 768

        self.fc1 = nn.Linear(
            1024,
            512
        )

        self.relu1 = nn.ReLU()

        self.dropout1 = nn.Dropout(
            dropout
        )

        self.fc2 = nn.Linear(
            512,
            256
        )

        self.relu2 = nn.ReLU()

        self.dropout2 = nn.Dropout(
            dropout
        )

        self.fc3 = nn.Linear(
            256,
            num_classes
        )

    def forward(
        self,
        speech_embedding,
        text_embedding
    ):

        fused = torch.cat(
            [
                speech_embedding,
                text_embedding
            ],
            dim=1
        )

        x = self.fc1(fused)

        x = self.relu1(x)

        x = self.dropout1(x)

        x = self.fc2(x)

        x = self.relu2(x)

        x = self.dropout2(x)

        logits = self.fc3(x)

        return logits

