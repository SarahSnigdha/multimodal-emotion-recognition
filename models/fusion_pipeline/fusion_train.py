def train_one_epoch():

    fusion_model.train()

    running_loss = 0

    all_preds = []
    all_labels = []

    for batch in tqdm(train_loader):

        audio = batch[
            "audio"
        ].to(DEVICE)

        input_ids = batch[
            "input_ids"
        ].to(DEVICE)

        attention_mask = batch[
            "attention_mask"
        ].to(DEVICE)

        labels = batch[
            "label"
        ].to(DEVICE)

        optimizer.zero_grad()

        with torch.no_grad():

            _, speech_embedding = speech_model(
                audio
            )

            _, text_embedding = text_model(
                input_ids,
                attention_mask
            )

        logits = fusion_model(
            speech_embedding,
            text_embedding
        )

        loss = criterion(
            logits,
            labels
        )

        loss.backward()

        optimizer.step()

        running_loss += loss.item()

        preds = torch.argmax(
            logits,
            dim=1
        )

        all_preds.extend(
            preds.cpu().numpy()
        )

        all_labels.extend(
            labels.cpu().numpy()
        )

    loss = (
        running_loss /
        len(train_loader)
    )

    acc = accuracy_score(
        all_labels,
        all_preds
    )

    return loss, acc

