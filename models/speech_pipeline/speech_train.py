def train_one_epoch(
    model,
    loader,
    optimizer
):

    model.train()

    running_loss = 0.0

    all_preds = []
    all_labels = []

    for input_values, labels in tqdm(loader):

        input_values = input_values.to(
            DEVICE
        )

        labels = labels.to(
            DEVICE
        )

        optimizer.zero_grad()

        logits, _ = model(
            input_values
        )

        loss = criterion(
            logits,
            labels
        )

        loss.backward()

        optimizer.step()

        running_loss += loss.item()

        preds = torch.argmax(
            logits,
            dim=1
        )

        all_preds.extend(
            preds.cpu().numpy()
        )

        all_labels.extend(
            labels.cpu().numpy()
        )

    epoch_loss = (
        running_loss /
        len(loader)
    )

    epoch_acc = accuracy_score(
        all_labels,
        all_preds
    )

    return (
        epoch_loss,
        epoch_acc
    )

