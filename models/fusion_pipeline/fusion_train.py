TARGET_SR = 16000
MAX_AUDIO_LENGTH = 4 * TARGET_SR

def preprocess_audio(audio_path):

    signal, sr = librosa.load(
        audio_path,
        sr=TARGET_SR
    )

    signal, _ = librosa.effects.trim(
        signal
    )

    signal = librosa.util.normalize(
        signal
    )

    if len(signal) > MAX_AUDIO_LENGTH:

        signal = signal[
            :MAX_AUDIO_LENGTH
        ]

    else:

        padding = (
            MAX_AUDIO_LENGTH
            - len(signal)
        )

        signal = np.pad(
            signal,
            (0, padding)
        )

    return signal.astype(
        np.float32
    )

