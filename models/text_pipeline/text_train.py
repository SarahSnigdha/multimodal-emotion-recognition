def validate(
    model,
    loader
):

    model.eval()

    running_loss = 0

    all_preds = []
    all_labels = []

    with torch.no_grad():

        for batch in loader:

            input_ids = batch[
                "input_ids"
            ].to(DEVICE)

            attention_mask = batch[
                "attention_mask"
            ].to(DEVICE)

            labels = batch[
                "label"
            ].to(DEVICE)

            logits, _ = model(
                input_ids,
                attention_mask
            )

            loss = criterion(
                logits,
                labels
            )

            running_loss += loss.item()

            preds = torch.argmax(
                logits,
                dim=1
            )

            all_preds.extend(
                preds.cpu().numpy()
            )

            all_labels.extend(
                labels.cpu().numpy()
            )

    val_loss = (
        running_loss /
        len(loader)
    )

    val_acc = accuracy_score(
        all_labels,
        all_preds
    )

    return (
        val_loss,
        val_acc
    )

