MAX_LEN = 16

class FusionDataset(Dataset):

    def __init__(self, dataframe):

        self.df = dataframe.reset_index(
            drop=True
        )

    def __len__(self):

        return len(self.df)

    def __getitem__(self, idx):

        row = self.df.iloc[idx]

        audio_path = row[
            "audio_path"
        ]

        text = row[
            "text"
        ]

        label = row[
            "label"
        ]

        # ====================================================
        # AUDIO
        # ====================================================

        audio = preprocess_audio(
            audio_path
        )

        # ====================================================
        # TEXT
        # ====================================================

        encoding = tokenizer(
            text,
            add_special_tokens=True,
            max_length=MAX_LEN,
            padding="max_length",
            truncation=True,
            return_attention_mask=True,
            return_tensors="pt"
        )

        return {
            "audio":
                torch.tensor(
                    audio,
                    dtype=torch.float
                ),

            "input_ids":
                encoding["input_ids"]
                .flatten(),

            "attention_mask":
                encoding["attention_mask"]
                .flatten(),

            "label":
                torch.tensor(
                    label,
                    dtype=torch.long
                )
        }

