def collate_fn(batch):

    audios = []
    labels = []

    for audio, label in batch:

        audios.append(
            audio.numpy()
        )

        labels.append(
            label
        )

    processed = feature_extractor(
        audios,
        sampling_rate=16000,
        return_tensors="pt",
        padding=True
    )

    input_values = processed[
        "input_values"
    ]

    labels = torch.tensor(
        labels,
        dtype=torch.long
    )

    return (
        input_values,
        labels
    )


