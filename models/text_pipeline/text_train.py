def train_one_epoch(
    model,
    loader,
    optimizer
):

    model.train()

    running_loss = 0

    all_preds = []
    all_labels = []

    for batch in tqdm(loader):

        input_ids = batch[
            "input_ids"
        ].to(DEVICE)

        attention_mask = batch[
            "attention_mask"
        ].to(DEVICE)

        labels = batch[
            "label"
        ].to(DEVICE)

        optimizer.zero_grad()

        logits, _ = model(
            input_ids,
            attention_mask
        )

        loss = criterion(
            logits,
            labels
        )

        loss.backward()

        optimizer.step()

        running_loss += loss.item()

        preds = torch.argmax(
            logits,
            dim=1
        )

        all_preds.extend(
            preds.cpu().numpy()
        )

        all_labels.extend(
            labels.cpu().numpy()
        )

    epoch_loss = (
        running_loss /
        len(loader)
    )

    epoch_acc = accuracy_score(
        all_labels,
        all_preds
    )

    return (
        epoch_loss,
        epoch_acc
    )

