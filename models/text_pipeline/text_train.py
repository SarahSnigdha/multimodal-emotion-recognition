        optimizer = torch.optim.AdamW(
            model.parameters(),
            lr=lr
        )

        patience_counter = 0

        best_epoch_acc = 0

        for epoch in range(
            NUM_EPOCHS
        ):

            train_loss, train_acc = (
                train_one_epoch(
                    model,
                    train_loader,
                    optimizer
                )
            )

            val_loss, val_acc = (
                validate(
                    model,
                    val_loader
                )
            )

            print(
                f"Epoch {epoch+1}/{NUM_EPOCHS} | "
                f"Train Acc={train_acc:.4f} | "
                f"Val Acc={val_acc:.4f}"
            )

            if val_acc > best_epoch_acc:

                best_epoch_acc = val_acc

                patience_counter = 0

            else:

                patience_counter += 1

            if val_acc > best_val_acc:

                best_val_acc = val_acc

                best_config = {
                    "learning_rate": lr,
                    "dropout": dropout
                }

                torch.save(
                    model.state_dict(),
                    best_model_path
                )

            if patience_counter >= PATIENCE:

                print(
                    "Early Stopping Triggered"
                )

                break

