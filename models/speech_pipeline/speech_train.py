class TESSSpeechDataset(Dataset):

    def __init__(self, dataframe):

        self.df = dataframe.reset_index(
            drop=True
        )

    def __len__(self):

        return len(self.df)

    def __getitem__(self, idx):

        row = self.df.iloc[idx]

        audio = preprocess_audio(
            row["path"]
        )

        label = row["label"]

        return (
            torch.tensor(
                audio,
                dtype=torch.float32
            ),
            label
        )


