TARGET_SR = 16000
MAX_AUDIO_LENGTH = 4
MAX_AUDIO_SAMPLES = TARGET_SR * MAX_AUDIO_LENGTH


def preprocess_audio(audio_path):

    signal, sr = librosa.load(
        audio_path,
        sr=TARGET_SR
    )

    signal, _ = librosa.effects.trim(
        signal,
        top_db=20
    )

    signal = librosa.util.normalize(
        signal
    )

    if len(signal) > MAX_AUDIO_SAMPLES:

        signal = signal[:MAX_AUDIO_SAMPLES]

    else:

        padding = (
            MAX_AUDIO_SAMPLES
            - len(signal)
        )

        signal = np.pad(
            signal,
            (0, padding),
            mode="constant"
        )

    return signal.astype(
        np.float32
    )


