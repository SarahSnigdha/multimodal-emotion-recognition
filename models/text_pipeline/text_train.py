def normalize_emotion_name(folder_name):

    folder_name = folder_name.lower()

    if "pleasant" in folder_name:
        return "pleasant_surprise"

    if "fear" in folder_name:
        return "fear"

    if "sad" in folder_name:
        return "sad"

    if "angry" in folder_name:
        return "angry"

    if "happy" in folder_name:
        return "happy"

    if "neutral" in folder_name:
        return "neutral"

    if "disgust" in folder_name:
        return "disgust"

    raise ValueError(
        f"Unknown folder: {folder_name}"
    )


def extract_word(filename):

    filename = filename.replace(
        ".wav",
        ""
    )

    parts = filename.split("_")

    return parts[1]


def build_dataset():

    data = []

    for root, dirs, files in os.walk(
        DATASET_PATH
    ):

        for file in files:

            if not file.endswith(".wav"):
                continue

            emotion = normalize_emotion_name(
                os.path.basename(root)
            )

            label = emotion_map[
                emotion
            ]

            word = extract_word(
                file
            )

            data.append(
                {
                    "text": word,
                    "emotion": emotion,
                    "label": label
                }
            )

    return pd.DataFrame(data)


